#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	if (n == 0)
		return (0);
	char *temp;
	size_t i;

	temp = (char *)s;
	i = 0;
	while (i < n)
	{
		temp[i] = '0';
		i++;
	}
}